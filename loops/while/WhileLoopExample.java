public class WhileLoopExample {
    public static void main(String[] args) {
        // While loop: counting from 1 to 5
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        // Do-while loop: counting from 1 to 5
        int j = 1;
        do {
            System.out.println(j);
            j++;
        } while (j <= 5);
    }
} 